package com.xiilab.lustre.model;

public class AmbariViewConfigVO {
	
	private String ambari_username;
	private String ambari_password;
	private String ambari_url;
	private String ambari_containername;
	
	
	public String getAmbari_username() {
		return ambari_username;
	}
	public void setAmbari_username(String ambari_username) {
		this.ambari_username = ambari_username;
	}
	public String getAmbari_password() {
		return ambari_password;
	}
	public void setAmbari_password(String ambari_password) {
		this.ambari_password = ambari_password;
	}
	public String getAmbari_url() {
		return ambari_url;
	}
	public void setAmbari_url(String ambari_url) {
		this.ambari_url = ambari_url;
	}
	public String getAmbari_containername() {
		return ambari_containername;
	}
	public void setAmbari_containername(String ambari_containername) {
		this.ambari_containername = ambari_containername;
	}
}
